// Draw a 1-bit bitmap (set bits = black) into the screen buffer
inline void drawBitmap1bit(const uint8_t* bmp, int bmpW, int bmpH, int ox, int oy) {
    int rowBytes = (bmpW + 7) / 8;
    for (int row = 0; row < bmpH; row++) {
        for (int col = 0; col < bmpW; col++) {
            if ((bmp[row * rowBytes + col / 8] >> (7 - col % 8)) & 1)
                Screen::drawPixel(ox + col, oy + row, 10);
        }
    }
}
